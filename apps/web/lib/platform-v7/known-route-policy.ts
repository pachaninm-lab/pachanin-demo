function normalizePath(value: string | null | undefined): string {
  const route = String(value || '').split('?')[0].replace(/\/$/, '');
  return route || PLATFORM_V7_ROUTE_ROOT;
}

export function isKnownPlatformV7Route(value: string | null | undefined): boolean {
  const pathname = normalizePath(value);
  return PLATFORM_V7_STATIC_ROUTES.has(pathname)
    || PLATFORM_V7_DYNAMIC_ROUTES.some((pattern) => pattern.test(pathname));
}

