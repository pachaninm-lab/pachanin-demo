function normaliseLocale(locale: string): AppLocale {
  return locale === 'en' || locale === 'zh' ? locale : 'ru';
}

function translator(messages: FlatCopy) {
  return (key: string) => {
    const value = messages[key];
    if (value === undefined) throw new Error(`Missing public landing copy: ${key}`);
    return value;
  };
}

export function getPublicLandingCopy(locale: string) {
  const messages = copy[normaliseLocale(locale)];
  return {
    t: translator(messages.landing),
    supporting: translator(messages.supporting),
    rolesCatalog: translator(messages.rolesCatalog),
  };
}
